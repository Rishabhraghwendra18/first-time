package variable.and.output;

/**
 *
 * @author Rishab R
 */
public class VariableAndOutput {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int myvariable=10;
        float mydecimalvalue=(float)4.5;
        double mydoublevalue=6.5;
        char mycharacter='A';
        boolean is_true=true;
        boolean is_false=false;
        System.out.println("Hello World");
        System.out.println(myvariable);
        System.out.println(mydecimalvalue);
        System.out.println(mydoublevalue);
        System.out.println(mycharacter);
        System.out.println(is_true);
        System.out.println(is_false);
    }
    
}
